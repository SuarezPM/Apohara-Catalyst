//! Generates `packages/apohara-shared/types.ts` from all `#[derive(TS)]` types
//! defined in apohara-types. CI runs this and diffs the output (see §0.7).

use std::path::PathBuf;
use ts_rs::TS;

// As more types get TS derives in other modules, add them here.
// Pattern: every type that crosses Rust↔TS boundary appears in this list.
fn main() {
    let args: Vec<String> = std::env::args().collect();
    let out_dir = args.get(1)
        .map(PathBuf::from)
        .unwrap_or_else(|| PathBuf::from("packages/apohara-shared"));
    std::fs::create_dir_all(&out_dir).expect("cannot create output dir");
    let out_file = out_dir.join("types.ts");

    // Placeholder: as Stage 2+ adds types with #[derive(TS)], they get exported here.
    // For Task 1.2 we just emit a header so the file exists for downstream tooling.
    let header = "// AUTO-GENERATED by crates/apohara-types/src/bin/generate_types.rs\n\
                  // DO NOT EDIT BY HAND. Run `bun run generate-types` to regenerate.\n\
                  // Generated: ".to_string() + &chrono::Utc::now().to_rfc3339() + "\n\n\
                  export const APOHARA_TYPES_VERSION = \"1.0.0-dev\";\n";

    std::fs::write(&out_file, header).expect("cannot write types.ts");
    println!("Generated: {}", out_file.display());
}
